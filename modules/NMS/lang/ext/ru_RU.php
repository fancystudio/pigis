<h4>Bounce Processing:</h4>
<p>The built in bounce processing capabilities allow reading a pop3 mailbox and searching the mails in that inbox for matching messages.  If the user can be properly detected, the \'bounce count\' for that user will be incremented.  Once the \'bounce limit\' is reached, the user will be disabled.</p>
<p><strong>Note:</strong> Your hosts email server configuration may be such that you don\'t receive bounce notifications in your pop3 mail box.  You may have to work with your hosting provider to ensure that this functionality works for you.</p>
<p>It is strongly recommended that you configure your sending email address to be the same as your pop3 email account, and that you use this account only for bounce processing.</p>

